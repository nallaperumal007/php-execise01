                    <th>Username</th>
                    <th>Encrypted Password</th>
                    <th>Decrypted Password</th>
                </tr>
            </thead>
            <tbody>
                <?php
                if ($result->num_rows > 0) {
                    while($row = $result->fetch_assoc()) {
                        $decrypted_password = decrypt_password($row['password']);
                        echo "<tr>
                                <td>{$row['id']}</td>
                                <td>{$row['name']}</td>
                                <td>{$row['username']}</td>
                                <td>{$row['password']}</td>
                                <td>{$decrypted_password}</td>
                              </tr>";
                    }
                } else {
                    echo "<tr><td colspan='5'>No records found</td></tr>";
                }
                ?>
            </tbody>
        </table>
    </div>
</body>
